
public class Pyramid {
    public static void main(String[] args) {
        
    
    int n = 4;
    for (int i = 1; i <= n; i++  ){
        for (int numbers = 1; numbers <= i;numbers++){
            System.out.print(numbers);
        }
        System.out.println();
    }
    
}
}

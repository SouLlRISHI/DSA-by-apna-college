public class Switch {
    public static void main(String[] args) {
       int num = 2;
       switch (num){
        
       case 1: System.out.println("mango");
       break;
       case 2: System.out.println("banana");
       break;
       case 3: System.out.println("okay");
       break;
       default : System.out.println("we wake up");
       }

    }

}
